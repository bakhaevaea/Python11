    # —Å–ø–∏—Å–æ–∫ –ø–æ–¥—Ö–æ–¥—è—â–∏—Ö –µ–º—É –¥–æ–º–æ–≤, –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –ø–æ –ø–ª–æ—â–∞–¥–∏.
    # –î–∞–Ω–Ω—ã–µ –æ –¥–æ–º–∞—Ö —Å—Ñ–æ—Ä–º–∏—Ä—É–π—Ç–µ —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º. –ü–ª–æ—â–∞–¥–∏ –æ—Ç 100 –¥–æ 300 –∫–≤. –º–µ—Ç—Ä–æ–≤,
    # —Ü–µ–Ω—ã –æ—Ç 3 –¥–æ 20 –º–ª–Ω.

    house = [ (random.randint(100,301),random.randint(3000000, 20000001), f'–¥–æ–º {i+1}')for i in range(15)]
    # —Å–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ –ø–ª–æ—â–∞–¥–∏
    house.sort(key = lambda x: x[0])
    square = []
    price_kv_m = []
    for i in range(15):
        a = int(round(house[i][1]/house[i][0], 0))
        if a < 50000:   # –æ—Ç–±–∏—Ä–∞–µ–º –¥–æ–º–∞, —Å—Ç–æ–∏–º–æ—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ –º–µ—Ç—Ä–∞ –∫–æ—Ç–æ—Ä—ã—Ö < 50000
            square.append(str(house[i][2]) + " - " + str(house[i][0]))
            price_kv_m.append(a)

    plt.bar(square, price_kv_m)
    plt.title('–¶–µ–Ω—ã –∑–∞ –∫–≤.–º')
    plt.xlabel('–¥–æ–º–∞')
    plt.ylabel('—Ü–µ–Ω–∞ –∫–≤.–º')
    ax = plt.gca()
